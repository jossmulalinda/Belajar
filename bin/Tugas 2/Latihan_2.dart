void main() {
  
  // Menyimpan panjang dan lebar dalam variabel double
  double panjang = 10;
  double lebar = 5;

  // Rumus untuk menghitung luas dan keliling
  double luas = panjang * lebar;
  double keliling = 2 * (panjang + lebar);

  // Berikut ini untuk menampilkan hasil
  print("Panjang = $panjang");
  print("Lebar = $lebar");
  print("Luas = $luas");
  print("Keliling = $keliling");
}